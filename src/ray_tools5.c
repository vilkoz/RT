#include "rtv1.h"

t_p3d		v_to_p(t_v3d v)
{
	t_p3d	p;

	p.x = v.x;
	p.y = v.y;
	p.z = v.z;
	return (p);
}

t_v3d		p_to_v(t_p3d v)
{
	t_v3d	p;

	p.x = v.x;
	p.y = v.y;
	p.z = v.z;
	return (p);
}
